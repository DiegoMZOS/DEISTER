class Rectangulo {
    ancho;
    alto;
  
    establecerAncho(ancho) {
      this.ancho = ancho;
    }
  
    establecerAlto(alto) {
      this.alto = alto;
    }
  
    calcularArea() {
      return ancho * alto;
    }
  }

  class Triangulo {
    ancho;
    alto;
  
    establecerAncho(ancho) {
      this.ancho = ancho;
    }
  
    establecerAlto(alto) {
      this.alto = alto;
    }
  
    calcularArea() {
      return ancho * alto;
    }
  }